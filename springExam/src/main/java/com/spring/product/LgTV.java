package com.spring.product;

public class LgTV {
	public void turnOn() {
		System.out.println("엘쥐TV전원을 킨다");
	}
	public void turnOff() {
		System.out.println("엘쥐TV전원을 끈다");
	}
	public void soundUP() {
		System.out.println("엘쥐TV소리를 올린다");
	}
	public void soundDown() {
		System.out.println("엘쥐TV소리를 내린다");
	}
}
